package com;

public class ClaseA {
	private String tampelota;
	private String color;
	private String tipopelota;
	
	public ClaseA() {
		
	}

	public ClaseA(String tampelota, String color, String tipopelota) {
		//super();
		this.tampelota = tampelota;
		this.color = color;
		this.tipopelota = tipopelota;
	}

	public String getTampelota() {
		return tampelota;
	}

	public void setTampelota(String tampelota) {
		this.tampelota = tampelota;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getTipopelota() {
		return tipopelota;
	}

	public void setTipopelota(String tipopelota) {
		this.tipopelota = tipopelota;
	}
		
	
}
